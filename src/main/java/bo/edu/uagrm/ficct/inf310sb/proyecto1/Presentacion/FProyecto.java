    private void RESULTADOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RESULTADOActionPerformed
       if(comprobarp()&&comp()){                        
        arbolPrueba = new ArbolBinario<>();
        PostFija r=new PostFija();
        r.setEntrada(PANTALLA.getText());  
        r.convertir();
        Queue<String>cola=r.colaDeCadenas();
        arbolPrueba.expressionTree(cola);

        double calculado=arbolPrueba.calcular();
        
        if(calculado%1==0){
            int cal = (int) calculado;
            RESULTADOP.setText(""+cal);
            INFIJA.setText("POSFIJA: " +r.toString());
            S="";
        }else{
            RESULTADOP.setText(String.valueOf(calculado));
            INFIJA.setText("POSFIJA: " +r.toString());
            S="";
        }
        }else
       {
           JOptionPane.showMessageDialog(null,"ERROR:VERIFIQUE LOS PARENTESIS Y OPERADORES","ERROR",0,null);           
       }
    }//GEN-LAST:event_RESULTADOActionPerformed

