    private boolean comprobarp(){//Verifica si los parentesis estan balanc
       String e=PANTALLA.getText();
        int i=0;
        int ayuda=0;
        while(i<e.length()){
            if(e.charAt(i)=='('){
                ayuda++;
            }else if(e.charAt(i)==')'){
                ayuda--;
            }
           if(ayuda<0){
               return false;
           }
           i++;
        }
        return (ayuda==0);
    }
    
    private boolean comp(){
        String e=PANTALLA.getText();
        int n=0;
        int A=0;
        while(n<e.length()&&A<2){
            if(!arbolPrueba.isOperator(String.valueOf(e.charAt(n)))){
                A=0; 
            }
            if (arbolPrueba.isOperator(String.valueOf(e.charAt(n)))) {
                A++;
            }
            n++;
        }
        return(A==0);
    }

